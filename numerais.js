 // adição
 console.log(10 + 10); // 20
 
 // subtração
 console.log(10 - 5); // 5

 // multiplicação
 console.log(10 * 5); // 50

 // divisão
 console.log(10 / 2 ); // 5

 // resto da divisão
 console.log(10 % 6); // 4 

